local Plugin = { 'nvim-lualine/lualine.nvim' }

Plugin.dependencies = { 'nvim-tree/nvim-web-devicons' }
Plugin.lazy = false

Plugin.opts = {
  options = {
    icons_enabled = true,
    theme = 'auto',
    component_separators = { left = '', right = ''},
    section_separators = { left = '', right = ''},
    disabled_filetypes = {
      statusline = {},
      winbar = {},
    },
    ignore_focus = {},
    always_divide_middle = true,
    globalstatus = false,
    refresh = {
      statusline = 1000,
      tabline = 1000,
      winbar = 1000,
    }
  },
  sections = {
    lualine_a = {'mode'},
    lualine_b = {'branch', 'diff', 'diagnostics'},
    lualine_c = {'filename'},
    lualine_x = {'encoding', 'fileformat', 'filetype'},
    lualine_y = {},
    lualine_z = {'location'}
  },
  inactive_sections = {
    lualine_a = {},
    lualine_b = {},
    lualine_c = {'filename'},
    lualine_x = {'location'},
    lualine_y = {},
    lualine_z = {}
  },
  tabline = {},
  winbar = {},
  inactive_winbar = {},
  extensions = {}
}

function Plugin.config(_, opts)
  local showmode = vim.api.nvim_get_option_value('showmode', {})
  if showmode then
    vim.api.nvim_set_option_value('showmode', false, {})
  end
  
  require('lualine').setup(opts)
end

return Plugin
